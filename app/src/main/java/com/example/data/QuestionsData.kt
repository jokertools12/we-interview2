            "Fiber" -> fiberQuestions + additionalFiberQuestions
            "CCNA" -> ccnaQuestions + additionalCcnaQuestions
            "Power" -> powerQuestions + additionalPowerQuestions
            "IT INFRASTRUCTURE" -> itInfraQuestions
